import mongoose from "mongoose";

const contactSchema = new mongoose.Schema(
  {
    // email, firstname, lastname, message, phone
    email: {
      type: String,
      required: true,
      trim: true,
    },
    firstname: {
      type: String,
      required: true,
      trim: true,
    },
    lastname: {
      type: String,
      required: true,
      trim: true,
    },
    message: {
      type: String,
      required: true,
      trim: true,
    },
    phone: {
      type: String,
      required: true,
      trim: true,
    },
  },
  {
    timestamps: true,
  }
);

const Contact =
  mongoose.models.contact || mongoose.model("contacts", contactSchema);

  export default Contact